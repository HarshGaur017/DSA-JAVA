public class BitwiseOperators {

    public static void main(String args[]) {
        System.out.println(5 & 6); //Binary AND (&)
        System.out.println(5 | 6); //Binary OR (|)
        System.out.println(5 ^ 6); //Binary XOR (^)
        System.out.println(~5); //Binary 1's Compliment (~)
        System.out.println(5<<2); //Binary Left Shift (<<) (Formula = a * 2^b)
        System.out.println(6>>1); //Binary Right Shift (>>) (Formula = a / 2^b)

    }    
}